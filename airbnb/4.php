                </div>

                <div class="w3-container">
                    <p>
                        Angst ist ein schlechter Berater. Diesen Satz haben wir alle oft gehört, doch, was
                        steckt hinter
                        diesem Satz und was hat es mit der finanziellen Freiheit zu tun? Die Antwort ist: Alles!
                    </p>
                    <p>
                        Angst begleitet uns durch das Leben: je mehr Angst man hat, desto kleiner dein
                        Spielplatz im Leben.
                        Es fängt klein auf: Die Eltern übertagen ihre Angst auf die Kinder in dem sie versuchen
                        diese von etwas zu bewahren. Es ist leichter für die dem Kind Angst einzujagen, als
                        diesem zu erklären, dass es keine Angst vor dem täglichen Leben haben soll.
                    </p>
                    <p>
                    <div align="center">
                        <img height="45%" src="images/fear.jpg" width="45%">
                    </div>
                    </p>
                    <p>
                        Erinnerst Du dich, als Du noch klein war und die Welt ohne Angst erlebt hast? Wo Du dich
                        getraut hast, Dinge zu tun, von welchen Du heute eher dich erschrecken würdest und nicht
                        mehr trauen würdest als Erwachsener? Wo ist der Geist geblieben, durch welchen Du bereit
                        warst die Welt um dich herum zu erkunden? Was ist mit dem Kind passiert, welches einst
                        lernte zu gehen, in dem es immer wieder gefallen war und dann wieder aufstand um weiter
                        zu
                        machen?
                    </p>
                    <p>
                        Das alles ist weg. Vergessen. Es herrscht nur Angst um eigene Sicherheit. Eingezäunt
                        durch alle möglichen Schranken im Kopf.
                    </p>
                    <p>Ein Beispiel für eine Schranke: <br><br>
                        <i><b>Flugangst</b></i>. Es gibt unzählige Menschen, welche eine Panik bekommen, sobald diese
                        darüber
                        nachdenken in den Flieger zu steigen. Davon sind ja nicht nur "Normalsterbliche" betroffen,
                        sondern auch viele Prominente, z.B. Fußballer, welche dann mit Auto zum Spiel gebracht werden
                        müssen, da ansonsten diese von der Angst nicht richtig spielen werden könnten. Ich selber hatte
                        immer wieder die Angst mit Leuten Live in Kontakt zu tretten. Ich habe früher es so gemacht,
                        dass ich der Angst auf die Kehle gestiegen bin und auf die Menschen dann gegangen bin. Bis ich
                        erfuhr, wie man es besser machen kann.
                    </p>
                    <p>
                    <div align="center">
                        <img height="45%" src="images/schranke.jpg" width="45%">
                    </div>
                    </p>
                    <p>
                        <b>Als Erstes muss man die Angst akzeptieren.</b> <br>
                        Es ist ein Teil unserer Natur, Produkt unserer Evolution,
                        wodurch unsere Vorfahren überlebt haben. In der heutigen Welt haben wir nicht so viele Gefahren,
                        wie wir uns mit der Angst ausmalen. Angst und tatsächliche Gefahr sind 2 verschiedene Paar
                        Schuhe.
                        Bleiben wir bei der Flugangst: die Wahrscheinlichkeit, dass beim Fliegen etwas passiert ist fast
                        immer gleich 0. Vor jedem Start der Maschine wird alles durchgecheckt und es ist
                        wahrscheinlicher
                        vom Blitz getroffen zu werden als etwas beim Fliegen passiert. Dennoch, haben manche Angst.
                        Diese
                        muss man einfach akzeptieren, dass die Angst da ist. Akzeptiere dich, du liebst dich trotzdem,
                        obwohl du Angst vor Flugangst (oder wovor du eben Angst hast), du bist kein Schwächling, kein
                        Versager,
                        einfach ein Mensch, welcher vor einer Herausforderung schiss hat.
                    </p>
                    <p>
                        <b>Zweites: Dich bei der Angst bedanken.</b><br>
                        Ja, liest sich bescheuert an, aber man muss der Angst "danke" sagen, dafür, dass es da ist.
                        Angst ist eine sehr starke, dominante Emotion, welche gewürdigt werden will und wenn wir diese
                        verdrängen, dann kommt sie mit der gleichen Kraft zurück. Aber wenn wir diese akzeptieren und
                        sich bedanken, dann geben wir ihr eine bestimmte Stelle und sie lässt nach (einfach probieren!).
                    </p>
                    <p>
                        <b>3. Verhältnis von Angst und Möglichkeiten umdrehen (was verliere ich, wenn ich es nicht
                            mache?)</b><br>
                        Was hält dich davor Reich und Frei zu werden? (<a href="5.html">Was ist eigentlich Freiheit?</a>)
                        Ganz simple Sache: Angst! Wenn du kurz davor bist, kurz vor einem Durchbruch oder einer
                        Entscheidung
                        du kommst an eine Schranke, welche dir die Angst gestellt hat. Du denkst, "was wäre wenn alles
                        schief
                        geht?" oder noch schlimmer "Ich hatte in Vergangenheit genau bei sowas Probleme gehabt...". Hier
                        musst du dir vor Augen die Möglichkeiten ausmalen, welche sich dir eröffnen würden, wenn du
                        trotz
                        der Angst durch die Schranke gehen würdest. Je mehr du dir ausmahlst, desto kleiner werden dir
                        die
                        Ängste erscheinen (am besten soll es im Verhältnis 5:1 sein).
                    </p>
                    <p>
                        <b>4. Handeln trotz der Angst, unsere Träume und Visionen nutzen.</b><br>
                        Nach den ersten 3 Tipps sollst du dich für das Fliegen entscheiden und dies trotzdem tun. Nutze
                        z.B. Logik: was kann ich machen um die Gefahr zu minimieren? Welche Maßnahmen kann ich treffen?
                        Warum hast du dich trotzdem für das Fliegen entschieden: Du willst ein Ziel am Ende der Reise
                        erreichen (siehe Punkt 3) und während du nachdenkst, welche Maßnahmen du konkret umsetzen wirst,
                        mekrst du sofort, wie die Angst immer kleiner wird.
                    </p>
                    <p>
                        <b>5. Nutze deine Referenzerfahrungen</b><br>
                        Wenn du einmal eine Angst "besiegt" hast, erinnere dich immer wieder daran, wenn du vor einer
                        neuen Angst stehst!
                    </p>
                    <p>
                        Merke dir eins:<br>
                        <b>Deine Angst zu versagen darf nie größer sein, als die Angst es nicht versucht zu haben!</b>
                    </p>
